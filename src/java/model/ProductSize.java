package model;

/**
 *
 * @author MSI GF
 */
public class ProductSize {
    private int pid;
    private int sizeId;
    private int quantity;

    public ProductSize() {
    }

    public ProductSize(int pid, int sizeId, int quantity) {
        this.pid = pid;
        this.sizeId = sizeId;
        this.quantity = quantity;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getSizeId() {
        return sizeId;
    }

    public void setSizeId(int sizeId) {
        this.sizeId = sizeId;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
    
}
